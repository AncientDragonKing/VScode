#include <iostream>
#include <string>
using namespace std;

int main() {
    int a = 10;         // 整數
    double b = 3.14;    // 小數
    char c = 'A';       // 字元
    string s = "Hello"; // 字串（要加 #include <string>）
    bool flag = true;   // 布林值（true/false）

    cout << "整數 a: " << a << endl;
    cout << "小數 b: " << b << endl;
    cout << "字元 c: " << c << endl;
    cout << "字串 s: " << s << endl;
    cout << "布林值 flag: " << flag << endl;

    return 0;
}